# In this project we calculate the area of the rectangle

length = float(input("Enter your length of the rectangle : "))
breath = float(input("Enter your breath of the rectangle : "))

# formula for the are is
area = length * breath
# display the area
print(f"Area of the rectangle is : {area}cm^2")


# in this we calculate the volume of the rectangle

length = float(input("Enter your length of the rectangle : "))
breath = float(input("Enter your breath of the rectangle : "))
height = float(input("Enter your height of the rectangle : "))

volume = length * breath * height

print(f"Volume of the rectangle is : {volume}cm^2")